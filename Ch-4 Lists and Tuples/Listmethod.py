# list methods 
# 1. list.sort method

# list = [2,34,4,1,43,10,12]
# # list.sort()
# print(list) 
# the result will be [1, 2, 4, 10, 12, 34, 43]

# 2. reverse method 
# list = [2,34,4,1,43,10,12]
# list.reverse()
# print(list) 
# the result will be [12, 10, 43, 1, 4, 34, 2]

# 3. append method
# list = [2,34,4,1,43,10,12]
# print(list)     #[2, 34, 4, 1, 43, 10, 12]
# list.append(99)
# print(list)        #the result will be    [2, 34, 4, 1, 43, 10, 12, 99]

# 4. insert method 
# list = [2, 34,4,1,43,10,12]
# print(list)         
# # [2, 34, 4, 1, 43, 10, 12]
# list.insert(0,30)
# print(list)
# # [30, 2, 34, 4, 1, 43, 10, 12]

# 5. pop method
# list = [2, 34,4,1,43,10,12]
# print(list)   
# # [2, 34, 4, 1, 43, 10, 12]
# list.pop(0)
# print(list) 
# #  [34, 4, 1, 43, 10, 12]

# 6. remove method 
list = [2, 34,4,1,43,10,12]
print(list)   
# [2, 34, 4, 1, 43, 10, 12]
list.remove(43)
print(list)
